package class3;

public class E5RelationalOperators {

    public static void main(String[] args) {

        System.out.println(10>15); // is 10 greater than 15 = false
        System.out.println(10<15); // is 10 less than 15 = true
        System.out.println(10==15); // == means = -- is 10 equal to 15 =false
        System.out.println(10!=15); // != means not equal -- is 10 not equal to 15 =true
        System.out.println(10>=15); //is 10 greater than or equal to 15 false
        System.out.println(10<=15); //is 10 LESS than or equal to 15 true overall answer is true

    }
}
